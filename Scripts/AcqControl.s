class AcqControl:JEM_Widget
{
	
	object msg
	number idleScreenState
	number extControl

	number isScreenUp(object self)
	{
		number x;JEM_getScreenAngle(x)
		self.setData("screen up",x==2)		
		number screen;self.getData("screen up",screen)
		return screen
	}
	
	object raiseScreen(object self)
	{
		JEM_raiseScreen();
		self.setData("screen up",1)
		return self
	}
	
	object lowerScreen(object self)
	{
		JEM_lowerScreen()
		self.setData("screen up",0)
		return self
	}
				
	object setScreen(object self,number screenUp)
	{
		if(screenUp)JEM_raiseScreen()
		else JEM_lowerScreen()
		self.setData("screen up",screenUp)
		return self
	}
	
	object controlRaiseScreen(object self)
	{
		number controlScreen;self.getData("control screen",controlScreen)
		if(!controlScreen)return self
		self.raiseScreen()
		return self
	}
	
	object controlLowerScreen(object self)
	{
		number controlScreen;self.getData("control screen",controlScreen)
		if(!controlScreen)return self
		self.lowerScreen()
		return self
	}
	
	object controlSetScreen(object self,number screenUp)
	{
		number controlScreen;self.getData("control screen",controlScreen)
		if(!controlScreen)return self
		number prevScreenUp;self.getData("screen up",prevScreenUp)
		self.setScreen(screenUp)
		if(screenUp!=prevScreenUp)Sleep(1);
		return self
	}

	object beginControlScreen(object self,number screenUp,number endScreenUp)
	{
		extControl=1
		number controlScreen;self.getData("control screen",controlScreen)
		if(!controlScreen)return self
		idleScreenState=endScreenUp
		number prevScreenUp;self.getData("screen up",prevScreenUp)
		self.setScreen(screenUp)
		if(screenUp!=prevScreenUp)Sleep(1);
		return self
	}

	object endControlScreen(object self)
	{
		extControl=0
		number controlScreen;self.getData("control screen",controlScreen)
		if(!controlScreen)return self
		self.setScreen(idleScreenState)
		return self
	}

	object controlFilament(object self,number setOn)
	{
		number controlFil;self.getData("control filament",controlFil)
		if(!controlFil)return self
		if(setOn)JEM_setFilamentOn()
		else JEM_setFilamentOff()	
		self.setData("control filament",0)
		self.setValues()
		return self
	}
	
	number getExtControl(object self)
	{
		return extControl
	}

	object setIdleScreenState(object self,number screenUp)
	{
		idleScreenState=screenUp
		return self
	}
	
	void stop(object self){setTerminate(1);}
	void pause(object self){setPaused(1);}
	void proceed(object self){setPaused(0);}
	void togglePause(object self){setPaused(1-getPaused());}	

//	
	AcqControl(object self)
	{
		self.setGroup(ACQCONTROL_sGroup)		
		self.setEcho(ACQCONTROL_echo)
		self.setName(ACQCONTROL_sName);		
		self.setTitle(ACQCONTROL_sTitle);		
		extControl=0
		setTerminate(0)
	}
		
	~AcqControl(object self)
	{
		self.unload()
	}
	
	object load(object self)
	{
		self.addData("not saved","online",ACQCONTROL_online)
		self.addData("not saved","shutter","neutral")		
		self.addData("screen up",0)		
		self.addData("control screen",1)
		self.addData("control filament",0)		
		self.addData("use SmartAcq",ACQCONTROL_useSMARTACQ)
		return self.super.load()
	}
	
	void setValues(object self)
	{
		if(!self.getFrameWindow().windowIsValid())return
		self.validateView()
		self.setPopup("shutter")
		self.setCheckBox("use SmartAcq")
		self.setCheckBox("control screen")
		self.setCheckBox("control filament")
		self.setButtonState("pause",getPaused())
	}

	void numberChanged(object self,string sIdent,number val)
	{
		self.super.numberChanged(sIdent,val)
		self.write()	
		self.setValues();
	}
	
	void popupChanged(object self,string sIdent,tagGroup itemTag)
	{
		self.super.popupChanged(sIdent,itemTag)
		if(sIdent=="shutter")
		{			
			string sShutter;self.getData("shutter",sShutter)
			if(sShutter=="open")JEM_setShutterPosition(1)
			if(sShutter=="closed")JEM_setShutterPosition(0)
			self.setData("shutter","neutral")
		}
		self.write()		
		self.setValues();
	}

	void boxChecked(object self,string sIdent,number val)
	{
		self.super.boxChecked(sIdent,val)
		self.write()
		self.setValues();
	}


	void screenUpPressed(object self)
	{
		if(self.getExtControl())
		{
			self.setIdleScreenState(1)
			if(optionDown())
				self.raiseScreen()
		}
		else
			self.raiseScreen()	
	}

	void screenDownPressed(object self)
	{
		if(self.getExtControl())
		{
			self.setIdleScreenState(0)
			if(optionDown())
				self.lowerScreen()
		}
		else
			self.lowerScreen()	
	}

	void stopPressed(object self)
	{
		self.stop()
	}
	
	void pausePressed(object self)
	{
		self.togglePause()
		self.setValues()
	}

